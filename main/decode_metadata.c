void md_decode_option(char* buf, void md_decode_opt()) {
    char tmp[2];
    int opt;

    tmp[0] = *md;
    tmp[1] = *(md + 1);

    opt = md_to_int(tmp);

    if (!opt) {
        md++;
        buf = NULL;
    }

    md++;
    md_decode_opt();
}

