void  md_decode_byte_array(char* buf) {
    int numb;
    char tmp[256];
    char* str;

    numb = md_decode_compact();
    str = tmp;

    while (numb) {
        *str = *md;
        str++; md++;
        numb--;
    }

    strcpy(buf, tmp);
}

void md_decode_storage_type(char* str) {
    char tmp[2];
    char name[256];
    char hasher[24];
    char key2_hasher[24];
    char key[256];
    char key2[256];
    char value[256];
    char unused[256];
    int idx;

    tmp[0] = *md;
    tmp[1] = *(md + 1);

    idx = md_to_int(tmp);

    if (!idx) {
        md++;
        md_decode_string(name);

        strcpy(str, name);
        return;
    } else if (idx == 1) {
        md++;
        md_decode_storage_hasher(hasher);
        md_decode_string(key);
        md_decode_string(value);
        md_decode_string(unused);

        sprintf(str, "%s###%s###%s###%s", hasher, key, value, unused);
        return;
    }

    md++;
    md_decode_storage_hasher(hasher);
    md_decode_string(key);
    md_decode_string(key2);
    md_decode_string(value);
    md_decode_storage_hasher(key2_hasher);


    sprintf(str, "%s###%s###%s###%s###%s", hasher, key, key2, value, key2_hasher);
    return;

}

void md_decode_storage_hasher(char* str) {
    char* hashers[7] = {"Blake2_128", "Blake2_256", "Blake2_128Concat", "Twox128", "Twox256", "Twox64Concat", "Identity"};
    char tmp[2];
    int idx;

    tmp[0] = *md;
    tmp[1] = *(md + 1);

    idx = md_to_int(tmp);

    strcpy(str, hashers[idx]);
}

void md_decode_storage_modifier(char* str) {
    char tmp[2];
    int idx;

    tmp[0] = *md;
    tmp[1] = *(md + 1);

    idx = md_to_int(tmp);
    md++;

    idx ? strcpy(str, "Default") : strcpy(str, "Optional");

}

void md_decode_string(char* buf) {
    int len;
    char tmp[256];
    char* str;

    len = md_decode_compact() * 2;
    str = tmp;

    while (len) {
        *str = *md;
        str++; md++;
        len--;
    }

    strcpy(buf, md_to_string(tmp));
}

static int md_decode_compact() {
    char raw[24];
    char* s;
    char* r;
    int flag, len;

    s = md;
    r = raw;

    raw[0] = *md;
    raw[1] = *(md + 1);

    flag = md_to_int(raw) & 0b11;

    if (!flag) {
        inc_md_by(1);
        return md_to_int(raw) >> 2;

    } else if (flag == 0b01) {
        inc_md_by(2);
        loop2(4, r, s);
        
        return md_to_int(raw) >> 2;

    } else if (flag == 0b10) {
        inc_md_by(4);
        loop2(8, r, s);

        return md_to_int(raw) >> 2;
    }

    len = (md_to_int(raw) >> 2) + 4;
    s += 2;

    loop2(len, r, s);
    inc_md_by(len);

    return md_to_int(raw);
}

char* md_to_string(char* str) {
    scale_vector vector = SCALE_VECTOR_INIT;
    scale_fixed_int fixed = { 0 };

    encode_u128_string_to_fixed_int_scale(&fixed, str);

    uint8_t serialized[16] = { 0 };
    size_t serialized_len = 0;
    serialize_fixed_int(serialized, &serialized_len, &fixed);

    push_vector(&vector, serialized, serialized_len);

    uint8_t bytes[32] = { 0 };
    size_t data_len = 0;
    serialize_vector(bytes, &data_len, &vector);

    clear_n_copy(str, bytes);

    return str;
}

int md_to_int(const char* str) {
    scale_fixed_int s_e;
    encode_fixed_hex_to_scale(&s_e, true, str);
    
    int32_t output = 0;
    decode_scale_fixed_int((void*)&output, &s_e);

    return output;
}